public class CWH_14_Do_While_Loop {
    public static void main(String[] args) {
        int b=0;
        do {
            System.out.println(b);
            b++;
        }while (b<5);

        int i=5;
        do {
            System.out.println(i);
            i--;
        }while (i>0);
    }
}
